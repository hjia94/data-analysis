class ProbeMotion(dict):
	"""Per-probe position dict (probe -> xyz array), as returned by the
	``read_probe_motion_*`` functions.

	Behaves exactly like the plain dict callers already unpack, but also carries
	a ``per_probe`` attribute: ``{probe: {'xpos', 'ypos', 'zpos', 'npos',
	'nshot'}}`` so multi-probe callers can recover each probe's full geometry.
	The flat ``xpos/ypos/zpos/npos/nshot`` returned alongside it refer to the
	first probe only.
	"""
	per_probe = None

